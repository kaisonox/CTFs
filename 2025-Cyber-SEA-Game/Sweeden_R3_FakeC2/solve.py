def rc4(data: bytes, key: bytes) -> bytes:
	S = list(range(256))
	j = 0
	for i in range(256):
		j = (j + S[i] + key[i % len(key)]) & 0xFF
		S[i], S[j] = S[j], S[i]
	i = j = 0
	out = bytearray()
	for b in data:
		i = (i + 1) & 0xFF
		j = (j + S[i]) & 0xFF
		S[i], S[j] = S[j], S[i]
		k = S[(S[i] + S[j]) & 0xFF]
		out.append(b ^ k)
	return bytes(out)

